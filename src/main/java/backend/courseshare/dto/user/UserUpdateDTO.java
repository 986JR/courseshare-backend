package backend.courseshare.dto.user;

import jakarta.validation.constraints.Email;
import jakarta.validation.constraints.NotBlank;
import jakarta.validation.constraints.Size;

public record UserUpdateDTO(
        @NotBlank
        @Size(min=3, max=100)
        String username,

        @NotBlank
        @Email
        String email,

        @NotBlank
        @Size(min=6)
        String password,
        String role
) {
}
